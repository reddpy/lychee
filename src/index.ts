// `clickedWindow` is the window passed into the menu item's click callback (typed
// as BaseWindow by Electron's API). Narrows to BrowserWindow and falls back to
// focused/first window so programmatic .click() (e.g. E2E tests, or any case
// where OS focus isn't on a Lychee window) still routes the event.
function sendMenuEvent(clickedWindow: BaseWindow | undefined, channel: string): void {
  const fromClick = clickedWindow instanceof BrowserWindow ? clickedWindow : null;
  const win = fromClick ?? BrowserWindow.getFocusedWindow() ?? BrowserWindow.getAllWindows()[0];
  win?.webContents.send(channel);
}

